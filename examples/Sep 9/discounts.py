weekday = input("What day of the week is it? ").lower().strip()

if weekday == "monday" or weekday == "mon":
    print("Moe's Monday")
elif weekday == "tuesday":
    print("Taco Tuesday")
elif weekday == "wednesday":
    print("Wings Wednesday")
elif weekday == "thursday":
    print("Thirsty Thursday")
else: 
    print("No discounts. ")



